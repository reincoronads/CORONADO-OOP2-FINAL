    private void logActivity(String description) {
        dbConnector dbc = new dbConnector();
        Connection conn = null;
        PreparedStatement pst = null;

        Session sess = Session.getInstance();

        try {
            String query = "INSERT INTO activity_logs (staff_id, description) VALUES (?, ?)";

            conn = dbc.getConnection();
            pst = conn.prepareStatement(query);

            pst.setInt(1, sess.getUid());
            pst.setString(2, description);

            pst.executeUpdate();
        } catch (SQLException ex) {
            System.err.println("Error logging activity: " + ex.getMessage());
            ex.printStackTrace();
        } finally {
            try {
                if (pst != null) pst.close();
                if (conn != null) conn.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }
    
    private void loadMembershipData() {
        dbConnector dbc = new dbConnector();
        try {
            String query = "SELECT m.*, u.user_Fname, u.user_Lname " +
                         "FROM memberships m " +
                         "JOIN tbl_user u ON m.member_id = u.user_id " +
                         "WHERE m.membership_id = ?";
            
            try (Connection conn = dbc.getConnection();
                 PreparedStatement pst = conn.prepareStatement(query)) {
                
                pst.setString(1, membershipId);
                try (ResultSet rs = pst.executeQuery()) {
                    if (rs.next()) {
                        // Set membership fields
                        memberID.setText(rs.getString("member_id"));
                        name.setText(rs.getString("user_Fname") + " " + rs.getString("user_Lname"));
                        payment.setText(rs.getString("payment_amount"));
                        
                        // Set combo box selections
                        setComboBoxSelection(userPlan, rs.getString("membership_plan"));
                        setComboBoxSelection(memStatus, rs.getString("member_status"));
                    }
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, 
                "Error loading membership data", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void setComboBoxSelection(JComboBox<String> combo, String value) {
        for (int i = 0; i < combo.getItemCount(); i++) {
            if (combo.getItemAt(i).equalsIgnoreCase(value)) {
                combo.setSelectedIndex(i);
                break;
            }
        }
    }


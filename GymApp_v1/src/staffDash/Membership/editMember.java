    private void submitButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitButtonMouseClicked
        // TODO add your handling code here:
                // Validate inputs
        if (payment.getText().isEmpty() || 
            userPlan.getSelectedItem() == null || 
            memStatus.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "All fields required", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        dbConnector dbc = new dbConnector();
        try (Connection conn = dbc.getConnection()) {
            String query = "UPDATE memberships SET " +
                          "member_status = ?, " +
                          "membership_plan = ?, " +
                          "payment_amount = ? " +
                          "WHERE membership_id = ?";

            try (PreparedStatement pst = conn.prepareStatement(query)) {
                pst.setString(1, memStatus.getSelectedItem().toString());
                pst.setString(2, userPlan.getSelectedItem().toString());
                pst.setDouble(3, Double.parseDouble(payment.getText()));
                pst.setString(4, membershipId);

                int updated = pst.executeUpdate();
                if (updated > 0) {
                    String activityDescription = "Edited Membership for: " + name.getText() + 
                                           " (ID: " + memberID.getText() + ")";
                    logActivity(activityDescription);
                    JOptionPane.showMessageDialog(this, "Membership updated", "Success", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_submitButtonMouseClicked


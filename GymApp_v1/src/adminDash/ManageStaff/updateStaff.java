    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // Trim all inputs first
        String firstName = fname.getText().trim();
        String lastName = lname.getText().trim();
        String birthDate = txtDate.getText().trim();
        String phone = mobileNum.getText().trim();
        String email = emailPane.getText().trim();
        String staffTypeStr = (staffType.getSelectedItem() != null) ? staffType.getSelectedItem().toString() : "";
        String staffStatusStr = (staffStatus.getSelectedItem() != null) ? staffStatus.getSelectedItem().toString() : "";

        // Validate required fields
        if (firstName.isEmpty() || lastName.isEmpty() || birthDate.isEmpty() || 
            phone.isEmpty() || staffTypeStr.isEmpty() || email.isEmpty() || staffStatusStr.isEmpty()) {
            showError("All fields are required and must be properly selected.");
            return;
        }

        // Validate email format
        if (!email.matches("^[\\w.-]+@[\\w.-]+\\.[a-zA-Z]{2,}$")) {
            showError("Please enter a valid email address.");
            return;
        }

        // Validate date format (MM-DD-YYYY)
        if (!birthDate.matches("^\\d{2}-\\d{2}-\\d{4}$")) {
            showError("Date must be in MM-DD-YYYY format.");
            return;
        }

        // Validate phone number (digits only, 10-15 chars)
        if (!phone.matches("^[0-9]{10,15}$")) {
            showError("Phone number must be 10-15 digits.");
            return;
        }

        // Check for duplicate email (excluding current staff)
        if (isEmailDuplicate(email, staffID.getText())) {
            showError("Email already exists. Please use a different email.");
            return;
        }

        // Database update
        dbConnector dbc = new dbConnector();
        Connection conn = null;
        PreparedStatement pst = null;

        try {
            String query = "UPDATE staff_tbl SET "
                + "staff_Fname = ?, "
                + "staff_Lname = ?, "
                + "staff_Birthdate = ?, "
                + "staff_Email = ?, "
                + "staff_Type = ?, "
                + "staff_Phone = ?, "
                + "staff_Status = ? "
                + "WHERE staff_id = ?";

            conn = dbc.getConnection();
            pst = conn.prepareStatement(query);

            pst.setString(1, firstName);
            pst.setString(2, lastName);
            pst.setString(3, birthDate);
            pst.setString(4, email);
            pst.setString(5, staffTypeStr);
            pst.setString(6, phone);
            pst.setString(7, staffStatusStr);
            pst.setString(8, staffID.getText().trim());

            int rowsUpdated = pst.executeUpdate();

            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this,
                    "Staff updated successfully!",
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE);

                // Refresh parent table if it exists
                if (parentTable != null) {
                    parentTable.refreshTable();
                }

                this.dispose(); // Close the form
            } else {
                JOptionPane.showMessageDialog(this,
                    "No staff found with the specified ID.",
                    "Warning",
                    JOptionPane.WARNING_MESSAGE);
            }
        } catch (SQLException ex) {
            showError("Database error: " + ex.getMessage());
            ex.printStackTrace();
        } finally {
            try {   
                if (pst != null) pst.close();
                if (conn != null) conn.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_submitButtonActionPerformed
  

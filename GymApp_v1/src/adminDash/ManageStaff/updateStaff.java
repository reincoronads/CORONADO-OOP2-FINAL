       private boolean isEmailDuplicate(String email, String currentStaffId) {
        dbConnector dbc = new dbConnector();
        try {
            String query = "SELECT COUNT(*) FROM staff_tbl WHERE staff_Email = ? AND staff_id != ?";
            Connection conn = dbc.getConnection();
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1, email);
            pst.setString(2, currentStaffId);
            
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                return rs.getInt(1) > 0;
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        return false;
    }
    

    public void refreshTable() {
        // Show loading cursor
        setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));

        // Execute refresh in background thread to prevent UI freezing
        new Thread(() -> {
            try {
                // Small delay to show loading feedback (optional)
                Thread.sleep(100);

                SwingUtilities.invokeLater(() -> {
                    try {
                        // Reload the STAFF data (changed from loadEquipmentData)
                        loadStaffData();

                        // Update the table model and UI
                        tableDarkWithEdit1.revalidate();
                        tableDarkWithEdit1.repaint();

                        // Optional: Show success message
                        JOptionPane.showMessageDialog(this,
                            "Staff data refreshed successfully",
                            "Refresh Complete",
                            JOptionPane.INFORMATION_MESSAGE);
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(this,
                            "Error refreshing staff table: " + ex.getMessage(),
                            "Refresh Error",
                            JOptionPane.ERROR_MESSAGE);
                    } finally {
                        // Restore default cursor
                        setCursor(Cursor.getDefaultCursor());
                    }
                });
            } catch (InterruptedException ex) {
                ex.printStackTrace();
                setCursor(Cursor.getDefaultCursor());
            }
        }).start();
    }
    
    private void refreshPaneMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_refreshPaneMouseClicked
        refreshPane.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));

        // Create a SwingWorker for better background task handling
        new SwingWorker<Void, Void>() {
            @Override
            protected Void doInBackground() throws Exception {
                // Simulate processing time (can be removed in production)
                Thread.sleep(200);
                return null;
            }

            @Override
            protected void done() {
                try {
                    get(); // Check for exceptions
                    refreshTable(); // Refresh STAFF data
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(updateStaffTable.this,
                        "Error refreshing staff data: " + ex.getMessage(),
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                } finally {
                    refreshPane.setCursor(Cursor.getDefaultCursor());
                }
            }
        }.execute();
    }//GEN-LAST:event_refreshPaneMouseClicked


     private void loadStaffData() {
        dbConnector dbc = new dbConnector();
        try {
            String query = "SELECT staff_id as 'ID', staff_Fname as 'First Name', " +
                         "staff_Lname as 'Last Name', staff_Email as 'Email', " +
                         "staff_Type as 'Type', staff_Status as 'Status' FROM staff_tbl";
            
            try (ResultSet rs = dbc.getData(query)) {
                tableDarkWithEdit1.setModel(DbUtils.resultSetToTableModel(rs));
                
                // Set column widths
                tableDarkWithEdit1.setColumnWidth(0, 50);   // ID column
                tableDarkWithEdit1.setColumnWidth(1, 150);  // First Name
                tableDarkWithEdit1.setColumnWidth(2, 150);  // Last Name
                tableDarkWithEdit1.setColumnWidth(3, 200);  // Email
                tableDarkWithEdit1.setColumnWidth(4, 100);  // Type
                tableDarkWithEdit1.setColumnWidth(5, 80);   // Status
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, 
                "Database Error: " + ex.getMessage(), 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        } finally {
            try {
                if (dbc.getConnection() != null) {
                    dbc.getConnection().close();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }
    

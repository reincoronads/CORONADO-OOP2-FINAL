    private void refreshPaneMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_refreshPaneMouseClicked
        // TODO add your handling code here:
        refreshPane.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
    
        new Thread(() -> {
            try {
                Thread.sleep(300); // Small delay for visual feedback

                SwingUtilities.invokeLater(() -> {
                    try {
                        refreshTable();
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(this,
                            "Error refreshing table: " + ex.getMessage(),
                            "Refresh Error",
                            JOptionPane.ERROR_MESSAGE);
                    } finally {
                        refreshPane.setCursor(Cursor.getDefaultCursor());
                    }
                });
            } catch (InterruptedException ex) {
                ex.printStackTrace();
            }
        }).start();
    }//GEN-LAST:event_refreshPaneMouseClicked


    public updateEquipTable() {
        initComponents();

        // Apply custom scrollbar styling
        tableDarkWithEdit1.fixTable(jScrollPane1);

        // Configure for equipment table
        tableDarkWithEdit1.configureForTable(
                "equipment_tbl", // Database table name
                "id",            // Actual ID column name in DB
                "ID"             // Display column name in table
        );

        tableDarkWithEdit1.setDbConnector(new dbConnector());

        // âœ… Automatically fetch user ID from session
        int currentUserId = Session.getInstance().getUid();
        tableDarkWithEdit1.setCurrentUserId(String.valueOf(currentUserId));

        loadEquipmentData();  // Load equipment data into table
    }

    void loadEquipmentData() {
        String query = "SELECT id as 'ID', name as 'Equipment Name', "
                + "description as 'Description', quantity as 'Quantity', "
                + "price as 'Price', vendor as 'Vendor', "
                + "address as 'Vendor Address' "  // Added this line
                + "FROM equipment_tbl";

        try (Connection conn = new dbConnector().getConnection();
             PreparedStatement pst = conn.prepareStatement(query);
             ResultSet rs = pst.executeQuery()) {

            tableDarkWithEdit1.setModel(DbUtils.resultSetToTableModel(rs));
            tableDarkWithEdit1.setDefaultRenderer(Object.class, tableDarkWithEdit1.cell);
            tableDarkWithEdit1.getTableHeader().setDefaultRenderer(tableDarkWithEdit1.header);

            // Set column widths & alignments
            tableDarkWithEdit1.setColumnWidth(0, 50);    // ID
            tableDarkWithEdit1.setColumnWidth(1, 200);   // Equipment Name
            tableDarkWithEdit1.setColumnWidth(2, 250);   // Description
            tableDarkWithEdit1.setColumnWidth(3, 80);    // Quantity
            tableDarkWithEdit1.setColumnWidth(4, 100);   // Price
            tableDarkWithEdit1.setColumnWidth(5, 150);   // Vendor
            tableDarkWithEdit1.setColumnWidth(6, 200);   // Vendor Address (new column)

            for (int i = 0; i < tableDarkWithEdit1.getColumnCount(); i++) {
                tableDarkWithEdit1.setCellAlignment(i, JLabel.LEFT);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,
                    "Database Error: " + ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
    
    public void refreshTable() {
        loadEquipmentData(); // This will reload the data from database
        tableDarkWithEdit1.revalidate(); // Update the UI
        tableDarkWithEdit1.repaint();
    }

